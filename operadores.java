package br.com.generation2;

public class operadores {

	public static void main(String[] args) {
		// Operadores de atribuição
		
		int x = 10;
		System.out.println(x);
		
		
		x = x + 25; // ou poderia escrever x += 25;
		System.out.println(x);
		
		
		x = x / 2; // ou poderia escrever x /= 2;
		System.out.println(x);
		
		
		x = x % 2; // ou poderia escrever x %= 2;
		System.out.println(x);
		
		

	}

}
